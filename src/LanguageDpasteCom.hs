dpasteCom :: Content -> Language -> ExpireTime -> Service
dpasteCom c l et = Service { s_endpoint = "/api/v2/"
                           , s_host     = "dpaste.com"
                           , s_port     = 443
                           , s_name     = "dpaste.com"
                           , s_nvs      = dpasteComNvs c l et
                           }

dpasteComNvs :: Content -> Language -> ExpireTime -> NameValues
dpasteComNvs c l et = NameValues
  [ ("content"    , c)
  , ("syntax", maybe "text" unLexer (dpasteComLanguages l))
  , ("expiry_days", dpasteComExpires et)
  -- , ("title" , "TODO")
  ]

{- Valid values are: 1-365 -}
dpasteComExpires :: ExpireTime -> Expires
dpasteComExpires = \case
  ExpDay    -> "1"
  ExpWeek   -> "7"
  ExpMonth  -> "31"
  ExpYear   -> "365"
  ExpDays d -> C.pack (show d)
  _         -> "7"

